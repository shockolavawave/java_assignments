public class arithmetic
{
    public static void main(String[] args)
    {
        float a = 5, b = 11;

        System.out.println("The two numbers are: " + a + " " + b);
        System.out.println('\n');

        System.out.println(a + " + " + b + " = " + (a + b));
        System.out.println(a + " - " + b + " = " + (a - b));
        System.out.println(a + " * " + b + " = " + (a * b));
        System.out.println(a + " / " + b + " = " + (a / b));
    }
}
