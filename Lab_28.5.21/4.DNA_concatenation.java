public class DNA_concatenation {
    public static void main(String[] args) {

        String dna1 = "AGCTAGA";
        String dna2 = "GCATGAACC";

        System.out.println("dna1 = " + dna1);
        System.out.println("dna2 = " + dna2 + '\n');

        System.out.println("dna1 + dna2 = " + (dna1 + dna2));

    }
}
